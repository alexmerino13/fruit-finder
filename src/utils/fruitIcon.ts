export default function fruitIcon(name: string) {
  switch (name) {
    case 'Banana':
      return '🍌';
    case 'Strawberry':
      return '🍓';
    case 'Blueberry':
      return '🫐';
    case 'Apple':
      return '🍎';
    case 'Green Apple':
      return '🍏';
    case 'Grape':
      return '🍇';
    case 'Watermelon':
      return '🍉';
    case 'Honeydew':
      return '🍈';
    case 'Avocado':
      return '🥑';
    case 'Mandarin':
      return '🍊';
    case 'Orange':
      return '🍊';
    case 'Peach':
      return '🍑';
    case 'Pineapple':
      return '🍍';
    case 'Cherry':
      return '🍒';
    case 'Pear':
      return '🍐';
    case 'Lemon':
      return '🍋';
    case 'Lime':
      return '🍋‍🟩';
    case 'Kiwi':
      return '🥝';
    case 'Mango':
      return '🥭';
    case 'Tangerine':
      return '🍊';
    default:
      return '🌳';
  }
}
